    private Funcionario carregarFuncionario() {
        return new Funcionario(tfNome.getText(), tfLogin.getText(), pfSenha.getPassword());
    }
    
    private void limparTela() {
        tfNome.setText("");
        tfLogin.setText("");
        pfSenha.setText("");
        pfConfirmarSenha.setText("");
        tfNome.requestFocus();
    }
    
    
    
