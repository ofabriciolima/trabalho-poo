    //**********EVENTOS**********//
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            if (!Arrays.equals(pfSenha.getPassword(), pfConfirmarSenha.getPassword())) {
                throw new Exception("Senhas não conferem");
            }
            gerenciadorFuncionarios.cadastrarFuncionario(carregarFuncionario());
            Utilidades.msgInformacao("Funcionário cadastrado");
            limparTela();   
        } catch (Exception ex) {
            Utilidades.msgErro(ex.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

